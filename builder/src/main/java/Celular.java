
public class Celular {

    private String marca;
    private String modelo;
    private String cor;
    private int fabricacao;
    private String memoria;
    private String processador;
    private int preco;


    public String getMarca() {
        return marca;
    }

    public Celular setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public Celular setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getCor() {
        return cor;
    }

    public Celular setCor(String cor) {
        this.cor = cor;
    }

    public int getFabricacao() {
        return fabricacao;
    }

    public Celular setFabricacao(int fabricacao) {
        this.fabricacao = fabricacao;
    }

    public String getMemoria() {
        return memoria;
    }

    public Celular setMemoria(String memoria) {
        this.memoria = memoria;
    }

    public String getProcessador() {
        return processador;
    }

    public Celular setProcessador(String processador) {
        this.processador = processador;
    }

    public int getPreco() {
        return preco;
    }

    public Celular setPreco(int preco) {
        this.preco = preco;
    }
}
